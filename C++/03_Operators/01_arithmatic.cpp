#include <iostream> 
using namespace std; 

int main() {
    int a, b; 
    cout << "Enter the first number: "; 
    cin >> a; 

    cout << "Enter the second number: "; 
    cin >> b; 

    cout << "a + b = " << a + b << endl; //addition 
    cout << "a - b = " << a - b << endl; //substraction 
    cout << "a * b = " << a * b << endl; //multiplication 
    cout << "a / b = " << a / b << endl; //division 
    cout << "a % b = " << a % b << endl; //modulus 

    return 0; 
}