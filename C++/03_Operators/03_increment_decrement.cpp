#include <iostream>
using namespace std; 

int main() {
    int num1 = 5; 
    int num2 = 5; 

    cout << "Orginal values: " << num1 << " " << num2 << endl;

    num1 ++ ; 
    num2 ++ ; 
    cout << "Incremented value: " << num1 << " " << num2 << endl;

    num1 --; 
    num2 --; 
    cout << "Decremented value: " << num1 << " " << num2 << endl; 

    return 0; 
}